package Basics;

public class MultipleVariables {
    public static void main(String[] args) {

    int x = 5;
    int y = 6;
    int z = 50;
    System.out.println(x + y + z);

          // OR 

    int P = 5, Q = 6, R = 50;
    System.out.println(P + Q + R);



    // One Value to Multiple Variables ðŸ‘‡ 

    int A, B, C;
    A = B = C = 50;
    System.out.println(A + B + C);


}

}

