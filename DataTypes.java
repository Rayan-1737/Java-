package Basics;

public class DataTypes {
/*  

Primitive Data Types:

Definition: Built-in basic data types provided by Java.

Primitive types always hold a value, whereas non-primitive types can be null

Examples:
byte, short, int, long (integer types)

Byte :- Stores whole numbers from -128 to 127
Short :- Stores whole numbers from -32,768 to 32,767
Int :- Stores whole numbers from -2,147,483,648 to 2,147,483,647
Long :- Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807



float, double (decimal types)   Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
char (character type)           Stores a single character/letter or ASCII values
boolean (true/false type)      	Stores true or false values

Storage: Directly stores the actual value.

Size: Fixed sizes (e.g., int is 4 bytes, float is 4 bytes).

Features:
Simple, faster to process.

Cannot call methods on primitive types.
 */
  
 
/* 

Non-Primitive Data Types:

Definition: User-defined or complex types created using classes, interfaces, or arrays.

Examples:
String, Array, Object, List, Map.

Storage: Stores the memory reference (address) to the actual object.

Size: No fixed size (depends on the object's content).

Features:
Can call methods on objects (e.g., String.length()).
Used for more complex operations and data storage.

*/

}


