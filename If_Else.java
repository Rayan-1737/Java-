package Conditional_Statements;

public class If_Else {

    public static void main(String[] args) {
        int a = 20;
        int b = 18;
        if (a > b) {
            System.out.println("a is greater than b");
        } 
        else 
        {
            System.out.println("a is not greater than b");
        }
    }

}

