package Array;

public class Loop_Through_Array {

    public static void main(String[] args) {
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

        for (int i = 0; i < cars.length; i++) {
          System.out.println(cars[i]);
        }
      }
    
}

