package Methods;

public class Parameters_and_Arguments {
    
    // 'name' is a parameter
    static void greet(String name) {  
        System.out.println("Hello, " + name);   // 'name' is a parameter
    }

    public static void main(String[] args) {
        greet("Rayan");  // "Rayan" is an argument
    }
    
    
}



